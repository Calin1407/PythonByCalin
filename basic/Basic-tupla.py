#Estructura de datos

mi_tupla = (10, 20, 30, 40, 50)

##metodos de las tuplas

print("\n", mi_tupla.count(20))#Cuenta cuantas veces aparece un elemento en la tupla

print("\n", mi_tupla.index(30))#Devuelve el indice de la primera aparicion del elemento especificado

print("\n", mi_tupla[2])#Accede al elemento en la posicion especificada

print("\n", len(mi_tupla))#Devuelve la longitud de la tupla

print("\n", mi_tupla[-1])#Accede al ultimo elemento de la tupla